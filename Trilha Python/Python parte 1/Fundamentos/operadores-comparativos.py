variavel = 5

# igual
if variavel == 5:
  print("Os valores são iguais")

# diferente
if variavel != 7:
  print("O valor é diferente de 7, ele não é igual a 7")

# maior
if variavel > 2:
  print("O valor é maior que 2")

# maior ou igual
if variavel >= 5:
  print("O valor da variavel é maior ou igual a 5")

# menor
if variavel < 7:
  print("O valor é menor que 7")

# menor ou igual
if variavel <= 7:
  print("O valor é menor ou igual a 7")
