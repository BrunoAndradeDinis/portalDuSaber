# tipo int - inteiro
idade = 27
ano = 1995

print(type(idade))
print(type(ano))

# tipo float 
altura = 1.75
peso = 60.0

print(type(altura))
print(type(peso))

# tipo bool / boolean
semana = False
feriado = True

print(type(semana))
print(type(feriado))

# tipo str / string
nome = "Bruno"
profissao = 'Desenvolvedor de software'

print(type(nome))
print(type(profissao))

# todos juntos
print(type(nome))
print(type(idade))
print(type(altura))
print(type(feriado))