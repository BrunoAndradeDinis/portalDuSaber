quatro = 4
dois = 2

# soma
soma = quatro + dois

print(soma)

# subtração
subtracao = quatro - dois

print(subtracao)

# multiplicação
multiplicacao = quatro * dois

print(multiplicacao)

# divisão
divisao = quatro / dois

print(divisao)

# divisão interna
divisao_interna = quatro // 2

print(divisao_interna)

# módulo - resto da divisão
modulo = quatro % dois

print(modulo)

# exponenciação
exponenciacao = quatro ** dois

print(exponenciacao)